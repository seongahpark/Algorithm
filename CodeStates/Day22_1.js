/*
    Day 22 - 1. addObjectProperty
    
    문제

    두 개의 객체와 키를 입력받아 첫번째 객체의 키에 두번째 객체를 할당해야 합니다.
    
    ----------------------------------------------------------------------
    출력

    별도의 리턴문(return statement)을 작성하지 않습니다.
*/
function addObjectProperty(obj1, property, obj2) {
    // 여기에 코드를 작성합니다
    obj1[property] = obj2
  }
  