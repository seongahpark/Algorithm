/*
    Day 25 - 2. functionParameter
    
    문제

    함수와 수(num)를 입력받아 num에 함수를 적용(apply)한 결과를 리턴해야 합니다.
    
    ----------------------------------------------------------------------
    출력

    주어진 함수의 리턴 타입을 따릅니다.

    주의

    입력받은 함수가 어떤 일을 하는지는 중요하지 않습니다.
*/

function functionParameter(func, num) {
    return func(num);
  }
  